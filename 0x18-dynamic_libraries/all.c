#include <stdio.h>

/**
 * add - add two integers
 * @a: first unt
 * @b: second int
 * Return: addition
 */
int add(int a, int b)
{
        return (a + b);
}
/**
 * sub - subtract two integers
 * @a: first unt
 * @b: second int
 * Return: subtraction
 */
int sub(int a, int b)
{
        return(a - b);
}
/**
 * mul - multiple two integers
 * @a: first unt
 * @b: second int
 * Return: multiplication 
 */
int mul(int a, int b)
{
        return(a * b);
}
/**
 * div - divide two integers
 * @a: first unt
 * @b: second int
 * Return: division 
 */
int div(int a, int b)
{
        return(a / b);
}
/**
 * mod- mod two integers
 * @a: first unt
 * @b: second int
 * Return: final result
 */
int mod(int a, int b)
{
        return(a % b);
}

